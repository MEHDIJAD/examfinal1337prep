#include <unistd.h>
int	main(int argc, char **argv)
{
	int i;
	int nb;
	i = 0;
	nb = 1;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			if (nb % 5 == 0 && nb % 3 == 0)
			{
				write(1, "5", 1);
			}
			else if (nb % 5 == 0)
			{
				write(1, "3", 1);
			}
			else if (nb % 3 == 0)
			{
				write(1, "5", 1);
			}
			else
				write(1, &argv[1][i], 1);
			nb++;
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
